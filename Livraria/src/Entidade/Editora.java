package Entidade;

public class Editora {
	private int cod;
	private String Nome;
	private String Site;

	public int getCod() {
		return cod;
	}

	public void setCod(int cod) {
		this.cod = cod;
	}

	public String getNome() {
		return Nome;
	}

	public void setNome(String nome) {
		this.Nome = nome;
	}

	public String getSite() {
		return Site;
	}

	public void setSite(String Site) {
		this.Site = Site;
	}
}
