package Entidade;

public class Autor {
	private int cod;
	private String nome;
	private String biografia;

	public int getCod() {
		return cod;
	}

	public void setCod(int cod) {
		this.cod = cod;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getbiografia() {
		return biografia;
	}

	public void setbiografia(String biografia) {
		this.biografia = biografia;
	}
}
